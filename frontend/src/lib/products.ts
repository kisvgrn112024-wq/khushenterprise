  if (typeof window !== 'undefined') {
    const saved = localStorage.getItem('ke_products');
    if (saved) {
      try {
        const parsed = JSON.parse(saved);
        if (Array.isArray(parsed) && parsed.length > 0) {
          return parsed;
        }
      } catch (e) {
        // parsing error, fallback to DB
      }
    }
  }
  return [...productsDB];
